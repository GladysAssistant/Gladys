const { BadParameters, ExternalIntegrationUnavailableError } = require('../../utils/coreErrors');
const { Error422 } = require('../../utils/httpErrors');
const { WEBSOCKET_MESSAGE_TYPES } = require('../../utils/constants');

// transport-level failures stay a 400 (integration unreachable), only an
// explicit refusal of the integration (success: false) becomes a 422
const TRANSPORT_ERRORS = ['EXTERNAL_INTEGRATION_NOT_CONNECTED', 'EXTERNAL_INTEGRATION_COMMAND_TIMEOUT'];

/**
 * @description Relay the OAuth2 provider redirect (code + state captured by
 * the generic front callback route) to the integration, which verifies the
 * state, exchanges the code for tokens and stores them off-schema through
 * POST /config. Tokens never transit through the front nor the Gladys
 * server. An explicit failure of the integration (invalid state, exchange
 * refused...) comes back as a 422 with its message.
 * @param {string} selector - The selector of the external integration.
 * @param {object} params - The relayed callback parameters.
 * @param {string} params.key - The oauth2 config_schema key.
 * @param {string} params.code - The authorization code of the provider.
 * @param {string} params.state - The anti-CSRF state generated by the integration.
 * @param {string} params.redirect_uri - The redirect_uri used for the authorize URL.
 * @returns {Promise<object>} Resolve with { success: true }.
 * @example
 * await gladys.externalIntegration.relayOAuthCallback('ext-dev-netatmo', {
 *   key: 'netatmo_account',
 *   code: 'abc',
 *   state: 'xyz',
 *   redirect_uri: 'https://my-gladys/dashboard/integration/device/external/ext-dev-netatmo/oauth-callback',
 * });
 */
async function relayOAuthCallback(selector, { key, code, state, redirect_uri: redirectUri } = {}) {
  if (typeof key !== 'string' || key.length === 0) {
    throw new BadParameters('key: must be a non-empty string');
  }
  if (typeof code !== 'string' || code.length === 0) {
    throw new BadParameters('code: must be a non-empty string');
  }
  if (typeof state !== 'string' || state.length === 0) {
    throw new BadParameters('state: must be a non-empty string');
  }
  if (typeof redirectUri !== 'string' || redirectUri.length === 0) {
    throw new BadParameters('redirect_uri: must be a non-empty string');
  }
  const service = await this.getBySelector(selector);
  const configSchema = (service.manifest && service.manifest.config_schema) || [];
  const field = configSchema.find((schemaField) => schemaField.key === key);
  if (!field || field.type !== 'oauth2') {
    throw new BadParameters(`config.${key}: not an oauth2 field`);
  }
  try {
    await this.sendCommand(service, WEBSOCKET_MESSAGE_TYPES.EXTERNAL_INTEGRATION.OAUTH_CALLBACK, {
      key,
      code,
      state,
      redirect_uri: redirectUri,
    });
  } catch (e) {
    if (e instanceof ExternalIntegrationUnavailableError && !TRANSPORT_ERRORS.includes(e.message)) {
      throw new Error422(e.message);
    }
    throw e;
  }
  return { success: true };
}

module.exports = {
  relayOAuthCallback,
};
