const crypto = require('crypto');

const { ForbiddenError } = require('../../utils/coreErrors');
const { isSendOnlyChannel } = require('./externalIntegration.getContactProfile');
const { LINK_CODE_CACHE_PREFIX, LINK_CODE_TTL_MS, LINK_CODE_LENGTH } = require('./constants');

// unambiguous alphabet (no 0/O, 1/I/L): the user types this code in a chat
const CODE_ALPHABET = 'ABCDEFGHJKMNPQRSTUVWXYZ23456789';

/**
 * @description Generate a short link code for the current user (clicked
 * "Link my account" in the UI of a communication integration). The code is
 * the critical consent: single use, 15 minutes TTL, bound to the user and
 * the integration, only ever generated by the user from the Gladys UI. The
 * user then sends it to the bot in the external channel, and the
 * integration calls POST /contact/link with it.
 * @param {string} selector - The selector of the external integration.
 * @param {string} userId - Id of the user linking their account.
 * @returns {Promise<object>} Resolve with { code, expires_at }.
 * @example
 * const { code } = await gladys.externalIntegration.createLinkCode('ext-john-gladys-telegram', user.id);
 */
async function createLinkCode(selector, userId) {
  const service = await this.getBySelector(selector);
  // code-based linking is the consent of a RECEIVING channel (proof of
  // control of the external account); a send-only notification channel has
  // no inbound path so no link — its identity is the contact_schema
  if (isSendOnlyChannel(service.manifest)) {
    throw new ForbiddenError('LINK_CODE_NOT_AVAILABLE');
  }
  let code = '';
  for (let i = 0; i < LINK_CODE_LENGTH; i += 1) {
    code += CODE_ALPHABET[crypto.randomInt(CODE_ALPHABET.length)];
  }
  const expiresAt = Date.now() + LINK_CODE_TTL_MS;
  this.cache.set(`${LINK_CODE_CACHE_PREFIX}:${service.id}:${code}`, { userId, expiresAt });
  return { code, expires_at: new Date(expiresAt).toISOString() };
}

module.exports = {
  createLinkCode,
};
